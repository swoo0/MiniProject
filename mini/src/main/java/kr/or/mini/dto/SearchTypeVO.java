package kr.or.mini.dto;

public class SearchTypeVO {

	private String subway;    // 역세권
	private String school;    // 학세권
	private String park;      // 숲세권
	private String mall;      // 몰세권
	private String hospital;  // 의세권
	private String animal24;  // 견세권
	
	public String getSubway() {
		return subway;
	}
	public void setSubway(String subway) {
		this.subway = subway;
	}
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public String getPark() {
		return park;
	}
	public void setPark(String park) {
		this.park = park;
	}
	public String getMall() {
		return mall;
	}
	public void setMall(String mall) {
		this.mall = mall;
	}
	public String getHospital() {
		return hospital;
	}
	public void setHospital(String hospital) {
		this.hospital = hospital;
	}
	public String getAnimal24() {
		return animal24;
	}
	public void setAnimal24(String animal24) {
		this.animal24 = animal24;
	}
	
	
}
